('*********Operadores Aritméticos*********')
n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro: '))

print('*****soma*****')
print('{} + {} == {} '.format(n1, n2, n1 + n2))

print('*****subtração*****')
print('{} - {} == {}'.format(n1, n2, (n1 - n2)))

print('*****multiplicação*****')
print('{} * {} == {}'.format(n1, n2, (n1 * n2)))

print('*****divisão*****')
print('{} / {} == {}'.format(n1, n2, (n1 / n2)))

print('*****exponenciação*****')
print('{} ** {} == {}'.format(n1, n2, (n1 ** n2)))

print('*****divisão inteira*****')
print('{} // {} == {}'.format(n1, n2, (n1 // n2)))

print('*****módulo*****')
print('{} % {} == {}'.format(n1, n2, (n1 % n2)))


